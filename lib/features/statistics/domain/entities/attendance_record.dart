class AttendanceRecord {
  final String studentId;
  final String name;
  final String className;
  final String faculty;
  final String status;
  final String method;
  final String time;

  AttendanceRecord({
    required this.studentId,
    required this.name,
    required this.className,
    required this.faculty,
    required this.status,
    required this.method,
    required this.time,
  });
}